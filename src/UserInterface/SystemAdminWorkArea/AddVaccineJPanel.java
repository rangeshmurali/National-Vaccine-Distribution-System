    private void addVaccineJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addVaccineJButtonActionPerformed
        String vaccineName = vaccineNameJTextField.getText();
        String diseasesName = diseasesNameJTextField.getText();
        
        Vaccine vaccine = ecoSystem.getVaccineDirectory().createVaccine();
        vaccine.setVaccineName(vaccineName);
        vaccine.setDiseasesName(diseasesName);
        Enterprise ent = null;
        for(Network network : ecoSystem.getNetworkDirectory()){
            for(Enterprise enterprise : network.getEnterpriseDirectory().getEnterprisesDirectory()){
                if(enterprise instanceof DistributorEnterprise){
                    ent = enterprise;
                }
            }
        }
        Organization o = null;
        for(Organization organization : ent.getOrganizationDriectory().getOrganizationDirectory()){
            if(organization instanceof InventoryOrganization){
                o = organization;
            }
        }
        o.getInventoryDirectory().createInventory(vaccine);
        
    }//GEN-LAST:event_addVaccineJButtonActionPerformed

